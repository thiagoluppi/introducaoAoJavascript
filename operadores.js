// operadores
console.log(2+2);
console.log(2-2);
console.log(2*2);
console.log(2/2);

// comparação

// igual
console.log(2 == 2);
console.log(2 == 3);

// diferente
console.log(2 != 2);

// Igual estrito (retorna true se o valor e o tipo forem iguais)
// console.log(2 === "2") => false => pois o tipo é diferente
// console.log(2 == "2") => true => apesar dos tipos serem diferentes, numa comparação não estrita isso não importa
console.log(2 === "2");
console.log(2 == "2");

// diferente estrito:
console.log(3 !== "3");

// maior ou menor:
console.log(2 > 3);
console.log(2 < 3);

// maior ou igual:
console.log(2 >= 2);
console.log(2 <= 2);